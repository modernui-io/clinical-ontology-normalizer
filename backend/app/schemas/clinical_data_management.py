class DataCleaningMetrics(BaseModel):
    """Aggregated data cleaning metrics for a trial."""

    trial_id: str
    trial_name: str
    total_queries: int = 0
    open_queries: int = 0
    answered_queries: int = 0
    closed_queries: int = 0
    cancelled_queries: int = 0
    requeried_queries: int = 0
    query_rate_per_page: float = Field(0.0, description="Queries per CRF page")
    auto_generated_percent: float = Field(0.0, description="% of queries auto-generated")
    queries_by_category: dict[str, int] = Field(default_factory=dict)
    validation_pass_rate: float = Field(0.0, description="% of validations that passed")
    total_validation_checks: int = 0
    failed_validation_checks: int = 0
    data_completeness_percent: float = 0.0


class QueryResolutionMetrics(BaseModel):
    """Metrics on query resolution performance."""

    trial_id: str
    avg_resolution_days: float = Field(0.0, description="Average days to resolve queries")
    median_resolution_days: float = Field(0.0, description="Median days to resolve queries")
    queries_resolved_within_7_days: int = 0
    queries_resolved_within_14_days: int = 0
    queries_resolved_after_14_days: int = 0
    total_resolved: int = 0
    requery_rate: float = Field(0.0, description="% of queries that were requeried")
    resolution_by_category: dict[str, float] = Field(default_factory=dict, description="Avg days by category")


class DatasetComparisonResult(BaseModel):
    """Result of comparing two dataset versions."""

    dataset_a_id: str
    dataset_a_name: str
    dataset_b_id: str
    dataset_b_name: str
    records_added: int = 0
    records_removed: int = 0
    records_modified: int = 0
    variables_added: int = 0
    variables_removed: int = 0
    completeness_delta: float = 0.0
    conformance_delta: float = 0.0
    summary: str = ""
