class ClinicalDataset(BaseModel):
    """A CDISC-compliant clinical dataset with lifecycle management."""

    model_config = ConfigDict(from_attributes=True)

    id: str = Field(..., description="Unique dataset identifier")
    trial_id: str = Field(..., description="Owning trial identifier")
    trial_name: str = Field(..., description="Human-readable trial name")
    name: str = Field(..., description="Dataset name (e.g. SDTM v1.0)")
    cdisc_standard: CDISCStandard = Field(..., description="CDISC standard")
    version: str = Field(..., description="Dataset version")
    status: DatasetStatus = Field(default=DatasetStatus.DRAFT, description="Current lifecycle status")
    lock_level: Optional[DataLockLevel] = Field(None, description="Current lock level")
    total_records: int = Field(default=0, description="Total records in dataset")
    total_variables: int = Field(default=0, description="Total variables across domains")
    completeness_percent: float = Field(default=0.0, description="Data completeness %")
    conformance_percent: float = Field(default=0.0, description="CDISC conformance %")
    created_at: datetime = Field(..., description="Creation timestamp")
    updated_at: datetime = Field(..., description="Last updated timestamp")
    frozen_at: Optional[datetime] = Field(None, description="When dataset was frozen")
    locked_at: Optional[datetime] = Field(None, description="When dataset was locked")
    locked_by: Optional[str] = Field(None, description="User who locked the dataset")
    release_notes: Optional[str] = Field(None, description="Release notes")


class ClinicalDatasetCreate(BaseModel):
    """Request to create a new clinical dataset."""

    trial_id: str
    trial_name: str
    name: str
    cdisc_standard: CDISCStandard
    version: str = "1.0"
    total_records: int = 0
    total_variables: int = 0


class ClinicalDatasetUpdate(BaseModel):
    """Request to update a clinical dataset."""

    name: Optional[str] = None
    version: Optional[str] = None
    total_records: Optional[int] = None
    total_variables: Optional[int] = None
    release_notes: Optional[str] = None


class DatasetFreezeRequest(BaseModel):
    """Request to freeze a clinical dataset."""

    frozen_by: str
    reason: Optional[str] = None


class DatasetLockRequest(BaseModel):
    """Request to lock a clinical dataset."""

    locked_by: str
    lock_level: DataLockLevel = DataLockLevel.SOFT_LOCK
    reason: Optional[str] = None


class DatasetReleaseRequest(BaseModel):
    """Request to release a clinical dataset."""

    released_by: str
    release_notes: str


class DatasetListResponse(BaseModel):
    """Paginated list of clinical datasets."""

    items: list[ClinicalDataset]
    total: int


class CDISCDomain(BaseModel):
    """A CDISC domain within a dataset (e.g. DM, AE, LB)."""

    model_config = ConfigDict(from_attributes=True)

    name: str = Field(..., description="Domain abbreviation (e.g. DM, AE, LB)")
    description: str = Field(..., description="Domain description")
    key_variables: list[str] = Field(default_factory=list, description="Key variables")
    required_variables: list[str] = Field(default_factory=list, description="Required variables")
    total_records: int = Field(default=0, description="Records in this domain")
    conformance_issues: int = Field(default=0, description="Conformance issues found")


class CDISCConformanceReport(BaseModel):
    """Report from running CDISC conformance checks."""

    dataset_id: str
    dataset_name: str
    standard: CDISCStandard
    domains_checked: int
    total_issues: int
    critical_issues: int
    warning_issues: int
    info_issues: int
    conformance_percent: float
    domain_results: list[CDISCDomainConformanceResult] = Field(default_factory=list)


class CDISCDomainConformanceResult(BaseModel):
    """Conformance result for a single CDISC domain."""

    domain: str
    total_checks: int
    passed: int
    failed: int
    issues: list[str] = Field(default_factory=list)


