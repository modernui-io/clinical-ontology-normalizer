class AuditTrailEntry(BaseModel):
    """An entry in the dataset audit trail."""

    model_config = ConfigDict(from_attributes=True)

    id: str = Field(..., description="Unique audit entry identifier")
    dataset_id: str = Field(..., description="Dataset this audit entry belongs to")
    action: str = Field(..., description="Action performed (CREATE, UPDATE, FREEZE, LOCK, etc.)")
    field_name: Optional[str] = Field(None, description="Field that was changed")
    old_value: Optional[str] = Field(None, description="Previous value")
    new_value: Optional[str] = Field(None, description="New value")
    reason: Optional[str] = Field(None, description="Reason for the change")
    actor: str = Field(..., description="User who performed the action")
    timestamp: datetime = Field(..., description="When the action occurred")


class AuditTrailListResponse(BaseModel):
    """Paginated list of audit trail entries."""

    items: list[AuditTrailEntry]
    total: int


# ---------------------------------------------------------------------------
# Metrics
