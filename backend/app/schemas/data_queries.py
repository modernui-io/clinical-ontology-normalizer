"""Pydantic schemas for Data Queries & Discrepancy Management (CLINICAL-18).

Manages clinical data query lifecycle: query generation and routing, response
tracking, auto-query rule engine, query aging reports, discrepancy resolution
workflow, and query metrics/analytics for CRO data management operations.
"""

from __future__ import annotations

from datetime import datetime
from enum import Enum

from pydantic import BaseModel, ConfigDict, Field


# ---------------------------------------------------------------------------
# Enums
# ---------------------------------------------------------------------------


class QueryStatus(str, Enum):
    """Lifecycle status of a data query."""

    OPEN = "open"
    ANSWERED = "answered"
    CLOSED = "closed"
    CANCELLED = "cancelled"
    REQUERIED = "requeried"


class QueryPriority(str, Enum):
    """Priority level of a data query."""

    CRITICAL = "critical"
    HIGH = "high"
    MEDIUM = "medium"
    LOW = "low"


class QueryCategory(str, Enum):
    """Category classifying the type of data discrepancy."""

    MISSING_DATA = "missing_data"
    INCONSISTENT = "inconsistent"
    PROTOCOL_DEVIATION = "protocol_deviation"
    OUT_OF_RANGE = "out_of_range"
    CODING_ERROR = "coding_error"
    CONSENT = "consent"
    OTHER = "other"


class QuerySource(str, Enum):
    """Source that generated the data query."""

    MANUAL = "manual"
    AUTO_RULE = "auto_rule"
    SDV = "sdv"
    MEDICAL_REVIEW = "medical_review"


class ResolutionType(str, Enum):
    """Type of discrepancy resolution."""

    DATA_CORRECTED = "data_corrected"
    CONFIRMED_CORRECT = "confirmed_correct"
    SOURCE_VERIFIED = "source_verified"
    QUERY_WITHDRAWN = "query_withdrawn"
    NOT_APPLICABLE = "not_applicable"


# ---------------------------------------------------------------------------
# Core records
# ---------------------------------------------------------------------------


class QueryResponse(BaseModel):
    """A response to a data query from a site or reviewer."""

    model_config = ConfigDict(from_attributes=True)

    id: str = Field(..., description="Unique response identifier")
    query_id: str = Field(..., description="Parent data query identifier")
    responder: str = Field(..., description="Name/role of the person responding")
    response_text: str = Field(..., description="Response text content")
    attachments: list[str] = Field(
        default_factory=list, description="List of attachment filenames"
    )
    created_at: datetime = Field(..., description="Response creation timestamp")


class DataQuery(BaseModel):
    """A data query record representing a discrepancy or question about clinical data."""

    model_config = ConfigDict(from_attributes=True)

    id: str = Field(..., description="Unique data query identifier")
    trial_id: str = Field(..., description="Trial identifier")
    site_id: str = Field(..., description="Site identifier where query originates")
    subject_id: str | None = Field(None, description="Subject/patient identifier")
    visit_name: str | None = Field(None, description="Visit name or number")
    form_name: str = Field(..., description="CRF form name")
    field_name: str = Field(..., description="Field name within the form")
    query_text: str = Field(..., description="Query text describing the discrepancy")
    status: QueryStatus = Field(
        default=QueryStatus.OPEN, description="Current query lifecycle status"
    )
    priority: QueryPriority = Field(
        default=QueryPriority.MEDIUM, description="Query priority level"
    )
    category: QueryCategory = Field(..., description="Query category")
    source: QuerySource = Field(..., description="Source that generated the query")
    assigned_to: str | None = Field(None, description="Person/role assigned to resolve")
    auto_rule_id: str | None = Field(
        None, description="Auto-query rule ID if generated by rule engine"
    )
    responses: list[QueryResponse] = Field(
        default_factory=list, description="Responses to this query"
    )
    opened_date: datetime = Field(..., description="Date query was opened")
    answered_date: datetime | None = Field(None, description="Date query was answered")
    closed_date: datetime | None = Field(None, description="Date query was closed")
    created_at: datetime = Field(..., description="Record creation timestamp")


class AutoQueryRule(BaseModel):
    """An automatic query generation rule for programmatic data checks."""

    model_config = ConfigDict(from_attributes=True)

    id: str = Field(..., description="Unique rule identifier")
    rule_name: str = Field(..., description="Human-readable rule name")
    condition: str = Field(
        ..., description="Condition expression (e.g., 'field IS NULL', 'value > ULN*3')"
    )
    form: str = Field(..., description="Target CRF form name")
    field: str = Field(..., description="Target field name")
    message_template: str = Field(
        ..., description="Query message template with placeholders"
    )
    category: QueryCategory = Field(
        default=QueryCategory.MISSING_DATA, description="Query category for generated queries"
    )
    priority: QueryPriority = Field(
        default=QueryPriority.MEDIUM, description="Default priority for generated queries"
    )
    active: bool = Field(default=True, description="Whether the rule is currently active")
    created_at: datetime = Field(..., description="Rule creation timestamp")


class DiscrepancyResolution(BaseModel):
    """Resolution details for a closed data query."""

    model_config = ConfigDict(from_attributes=True)

    query_id: str = Field(..., description="Data query identifier")
    resolution_type: ResolutionType = Field(..., description="Type of resolution applied")
    resolution_notes: str = Field(..., description="Notes describing the resolution")
    resolved_by: str = Field(..., description="Person who resolved the discrepancy")
    resolved_date: datetime = Field(..., description="Date of resolution")


