class QueryCloseRequest(BaseModel):
    """Request to close a data query with resolution details."""

    model_config = ConfigDict(from_attributes=True)

    resolution_type: ResolutionType = Field(..., description="Type of resolution")
    resolution_notes: str = Field(..., description="Resolution notes")
    resolved_by: str = Field(..., description="Person resolving")


class BulkCloseRequest(BaseModel):
    """Request to close multiple queries at once."""

    model_config = ConfigDict(from_attributes=True)

    query_ids: list[str] = Field(..., description="List of query IDs to close")
    resolution_type: ResolutionType = Field(..., description="Resolution type for all")
    resolution_notes: str = Field(..., description="Resolution notes")
    resolved_by: str = Field(..., description="Person resolving")


class BulkAssignRequest(BaseModel):
    """Request to assign multiple queries to a person."""

    model_config = ConfigDict(from_attributes=True)

    query_ids: list[str] = Field(..., description="List of query IDs to assign")
    assigned_to: str = Field(..., description="Person/role to assign queries to")


# ---------------------------------------------------------------------------
# Reporting
# ---------------------------------------------------------------------------


class AgingBucket(BaseModel):
    """Query counts for a single aging time bucket."""

    model_config = ConfigDict(from_attributes=True)

    bucket: str = Field(..., description="Age bucket label (e.g., '0-7d')")
    count: int = Field(ge=0, description="Number of queries in this bucket")
    query_ids: list[str] = Field(
        default_factory=list, description="Query IDs in this bucket"
    )


class QueryAgingReport(BaseModel):
    """Report showing open queries grouped by age buckets."""

    model_config = ConfigDict(from_attributes=True)

    total_open: int = Field(ge=0, description="Total open queries")
    buckets: list[AgingBucket] = Field(
        default_factory=list, description="Aging buckets"
    )
    oldest_query_days: int = Field(
        ge=0, description="Age of the oldest open query in days"
    )
    generated_at: datetime = Field(..., description="Report generation timestamp")


class SiteQuerySummary(BaseModel):
    """Query summary for a single site."""

    model_config = ConfigDict(from_attributes=True)

    site_id: str = Field(..., description="Site identifier")
    total_queries: int = Field(ge=0, description="Total queries for this site")
    open_queries: int = Field(ge=0, description="Open queries")
    answered_queries: int = Field(ge=0, description="Answered queries")
    closed_queries: int = Field(ge=0, description="Closed queries")
    requeried_queries: int = Field(ge=0, description="Re-queried queries")
    avg_resolution_days: float = Field(
        ge=0.0, description="Average days to close queries"
    )


class QueryMetrics(BaseModel):
    """Aggregated data query metrics dashboard."""

    model_config = ConfigDict(from_attributes=True)

    total_queries: int = Field(ge=0, description="Total queries across all trials")
    open_queries: int = Field(ge=0, description="Currently open queries")
    answered_queries: int = Field(ge=0, description="Queries with responses pending review")
    closed_queries: int = Field(ge=0, description="Closed/resolved queries")
    cancelled_queries: int = Field(ge=0, description="Cancelled queries")
    requeried_queries: int = Field(ge=0, description="Re-queried queries")
    avg_resolution_days: float = Field(
        ge=0.0, description="Average days from open to close"
    )
    queries_by_category: dict[str, int] = Field(
        default_factory=dict, description="Query counts by category"
    )
    queries_by_site: dict[str, int] = Field(
        default_factory=dict, description="Query counts by site"
    )
    queries_by_priority: dict[str, int] = Field(
        default_factory=dict, description="Query counts by priority"
    )
    auto_query_count: int = Field(
        ge=0, description="Queries generated by auto-rules"
    )
    manual_query_count: int = Field(
        ge=0, description="Queries created manually"
    )


